function checkoutWhatsApp() {
    if (cart.length === 0) return alert("Your bag is empty!");

    // 1. Get ALL image links from the cart and join them with a new line (%0A)
    let allImageLinks = cart.map(item => item.img).join('%0A'); 
    
    // 2. Build the list of names and prices for the text area
    let itemsList = cart.map(i => `• *${i.name}* (₹${i.price})`).join('%0A');
    let total = document.getElementById('cart-total').innerText;

    // 3. Construct the message with all links at the top
    let message = `*PRODUCT IMAGES:*%0A${allImageLinks}%0A%0A*THE STYLIST - NEW ORDER*%0A${itemsList}%0A%0A*TOTAL: ₹${total}*`;

    // 4. Open WhatsApp
    window.open(`https://wa.me/${myNumber}?text=${message}`);
}
// Function to check if a specific product was requested via the URL
window.onload = function() {
    const urlParams = new URLSearchParams(window.location.search);
    const productId = urlParams.get('id'); // Looks for ?id=XX in the link

    if (productId) {
        // Find the specific product card
        const targetProduct = document.querySelector(`[data-id="${productId}"]`);
        
        if (targetProduct) {
            // Scroll to the suit smoothly
            targetProduct.scrollIntoView({ behavior: 'smooth', block: 'center' });
            
            // Add a "Highlight" effect so the user knows this is the one from the ad
            targetProduct.style.border = "2px solid var(--accent)";
            targetProduct.style.boxShadow = "0 0 20px var(--accent)";
            
            // Optional: Automatically open the hover info
            targetProduct.querySelector('.product-info-overlay').style.opacity = "1";
            targetProduct.querySelector('.product-info-overlay').style.transform = "translateY(0)";
        }
    }
}


