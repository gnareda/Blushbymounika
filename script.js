// Function to generate a unique name for each of the 160 images
function generateSuitName(i) {
    const color = colors[i % colors.length];
    const fabric = fabrics[i % fabrics.length];
    const style = styles[i % styles.length];
    return `${color} ${fabric} ${style}`;
}

for (let i = 0; i < suitImageLinks.length; i++) {
    const liveImgPath = suitImageLinks[i]; // Uses the Postimg link
    const localImgPath = `images/Product (${i + 1}).jpeg`; // For your website display
    const suitName = generateSuitName(i); 
    const price = 295;

    const card = document.createElement('div');
    card.className = 'product-card';
    card.setAttribute('data-id', i + 1);
    
    card.innerHTML = `
        <div class="product-img">
            <img src="${localImgPath}" alt="${suitName}" onerror="this.src='https://via.placeholder.com/300x400?text=Suit'">
        </div>
        <div class="product-info-overlay">
            <h3>${suitName}</h3>
            <p class="price">₹${price}</p>
            <button class="add-to-cart-btn"onclick="addToCart('${suitName}', ${price}, '${liveImgPath}')">Add to Bag</button>
        </div>
    `;
    grid.appendChild(card);
}

function addToCart(name, price, img) {
    cart.push({ name, price, img });
    updateUI();
    // Open sidebar
    document.getElementById('cart-sidebar').classList.add('active');
}

function removeFromCart(index) {
    cart.splice(index, 1);
    updateUI();
}

function updateUI() {
    const count = document.getElementById('cart-count');
    const list = document.getElementById('cart-items-list');
    const totalDisplay = document.getElementById('cart-total');
    
    
    count.innerText = cart.length;
    let total = 0;
    list.innerHTML = "";
    
    cart.forEach((item, index) => {
        total += item.price;
        list.innerHTML += `
            <div class="cart-item">
                <img src="${item.img}" class="cart-item-img">
                <div class="cart-item-info">
                    <p>${item.name}</p>
                    <span>₹${item.price}</span>
                </div>
                <span class="remove-btn" onclick="removeFromCart(${index})">Remove</span>
            </div>
        `;
    });
    
    totalDisplay.innerText = total;
}

function toggleCart() {
    document.getElementById('cart-sidebar').classList.toggle('active');
}

function checkoutWhatsApp() {
    if (cart.length === 0) return alert("Your bag is empty!");

    // 1. Get ALL image links from the cart and join them with a new line (%0A)
    let allImageLinks = cart.map(item => item.img).join('%0A'); 
    
    // 2. Build the list of names and prices for the text area
    let itemsList = cart.map(i => `• *${i.name}* (₹${i.price})`).join('%0A');
    let total = document.getElementById('cart-total').innerText;

    // 3. Construct the message with all links at the top
    let message = `*PRODUCT IMAGES:*%0A${allImageLinks}%0A%0A*THE STYLIST - NEW ORDER*%0A${itemsList}%0A%0A*TOTAL: ₹${total}*`;

    // 4. Open WhatsApp
    window.open(`https://wa.me/${myNumber}?text=${message}`);
}
// Function to check if a specific product was requested via the URL
window.onload = function() {
    const urlParams = new URLSearchParams(window.location.search);
    const productId = urlParams.get('id'); // Looks for ?id=XX in the link

    if (productId) {
        // Find the specific product card
        const targetProduct = document.querySelector(`[data-id="${productId}"]`);
        
        if (targetProduct) {
            // Scroll to the suit smoothly
            targetProduct.scrollIntoView({ behavior: 'smooth', block: 'center' });
            
            // Add a "Highlight" effect so the user knows this is the one from the ad
            targetProduct.style.border = "2px solid var(--accent)";
            targetProduct.style.boxShadow = "0 0 20px var(--accent)";
            
            // Optional: Automatically open the hover info
            targetProduct.querySelector('.product-info-overlay').style.opacity = "1";
            targetProduct.querySelector('.product-info-overlay').style.transform = "translateY(0)";
        }
    }
}



function filterSuits() {
    const input = document.getElementById('suitSearch').value.toLowerCase();
    const cards = document.getElementsByClassName('product-card');

    for (let i = 0; i < cards.length; i++) {
        const title = cards[i].getElementsByTagName('h3')[0].innerText.toLowerCase();
        if (title.includes(input)) {
            cards[i].style.display = ""; // Show
        } else {
            cards[i].style.display = "none"; // Hide
        }
    }
}

// Mobile Search Toggle Logic
const searchBtn = document.getElementById('search-toggle');
const searchClose = document.getElementById('search-close');
const searchContainer = document.getElementById('searchContainer');
const searchInput = document.getElementById('suitSearch');


searchInput.addEventListener('input', function() {
    // If user deletes everything (length is 0)
    if (this.value.length === 0) {
        // Close the panel automatically after a tiny delay
        setTimeout(() => {
            if (this.value.length === 0) {
                searchContainer.classList.remove('active');
                filterSuits(); // Reset grid to show all suits
            }
        }, 200); 
    }
});

searchBtn.addEventListener('click', () => {
    searchContainer.classList.add('active');
    searchInput.focus(); // Auto-focuses so user can type immediately
});

searchClose.addEventListener('click', () => {
    searchContainer.classList.remove('active');
    searchInput.value = ''; // Clear search on close
    filterSuits(); // Show all suits again
});


